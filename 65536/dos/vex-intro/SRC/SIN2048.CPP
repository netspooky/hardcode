long* SinTab;

static void InstallSinTab()
{
  SinTab=new long[2048];
  for (int i=0; i<512; i++)
    SinTab[i]=sintab[i];
  for (i=1; i<512; i++)
    SinTab[1024-i]=SinTab[i];
  SinTab[512]=65536;
  for (i=0; i<1024; i++)
    SinTab[i+1024]=-SinTab[i];
}

static void FreeSinTab()
{
  delete SinTab;
}

